--CREATE

CREATE TABLE Person (
	Id UNIQUEIDENTIFIER CONSTRAINT person_id PRIMARY KEY default NEWID(),
	Name VARCHAR(15) NOT NULL,
    Surname VARCHAR(15) NOT NULL
);

CREATE TABLE Receipt (
	Id UNIQUEIDENTIFIER CONSTRAINT person_pk PRIMARY KEY default NEWID(),
	BuyerId UNIQUEIDENTIFIER CONSTRAINT receipt_person_fk REFERENCES Person(Id) NOT NULL,
	Date DATE default GETDATE() NOT NULL,
	Total MONEY NOT NULL
);
